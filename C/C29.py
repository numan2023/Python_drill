# 九九表の出力

# 九九表を出力してください。具体的には、出力のi行j列目 (1 ≦ i, j ≦ 9) の数値は i * j になるように出力してください。
# ただし、数値の間には半角スペースを、各行の末尾には、半角スペースの代わりに改行を入れてください。

# 期待する出力
# 答えの数値を出力してください。


# 1 2 3 4 5 ...
# 2 4 6 8 ...
# 3 6 9 ...
# ...
# 9 18 27 ... 81

# GPT
for i in range(1, 10):
    for j in range(1, 10):
        print(i * j, end=" ")
    print()

# or 模範解答
for i in range(1, 10):
    for j in range(1, 10):
        if j == 9:
            print(i * j)
        else:
            print(i * j, end=" ")