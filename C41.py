# 数値を半角スペース埋めで出力
# 自然数 N が与えられます。N が 3 けたになるよう数値の前に半角スペースを埋めて出力してください。

# 入力される値
# N
# 期待する出力
# 答えの数値を解答の形式に従った形で出力してください。
# N

# 条件
# すべてのテストケースにおいて、以下の条件をみたします。

# * N は自然数
# * N は 0 以上 999 以下

n = int(input())

if len(n) == 3:
    print(n)
else:
    print(f"{n:3d}")

# GPT
def print_padded_number(N):
    # Nを3桁の幅で半角スペース埋めして出力
    print(f"{N:3d}")
    # f{...}は、f-stringという機能でPython3.6以降で利用可能
    # {N:3d}
      # Nは出力する値
      # :はフォーマット指定子の開始を意味
      # 3dはフォーマット指定子のこと。dは整数値を意味。

# 入力
N_example = int(input())
print_padded_number(N_example)


# or 模範解答
N = input()
print("{: >3}".format(N))
# 「x桁に右揃えで、足りない部分はiで埋める」は"{:i>x}"で定義。
# ": >3"は、フォーマット指定子。
  # ：はフォーマット指定子の開始
  # >は文字列や数値を右揃えすることを意味する。左側に必要なだけスペースが挿入される
  # 3は全体の文字数・数値の幅を指定する＝3文字