# 九九表を罫線入りで出力

# 九九表を、横の数値間では | (半角スペース 2 つとバーティカルライン)、縦の数値間では = で区切って出力してください。
# ただし、数値を出力する際は 2 けたになるよう半角スペース埋めで出力します。また、縦の数値間で = を出力する際は、その上の行と文字数が等しくなるように出力します。

# 入力される値
# なし

# 期待する出力
# 答えの文字列を出力してください。
# S1
# S2
# S3
# ...
# S9


for i in range(1, 10):
    for j in range(1, 10):
        print("{: >2}".format(i * j), end="")
        if j == 9:
            print()
        else:
            print(end=" | ")

    if i < 9:
        print("=" * (2 * 9 + 3 * (9 - 1)))

